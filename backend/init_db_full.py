def init_all_spots():
    """初始化所有景点数据"""
    db = SessionLocal()
    
    # 检查是否已有数据
    existing_count = db.query(ScenicSpot).count()
    if existing_count > 50:  # 如果已有足够数据，跳过
        print(f"Data already exists ({existing_count} spots), skipping init")
        db.close()
        return
    
    print("Starting full data initialization...")
    
    spots_added = 0
    spot_id = 1
    
    for city, spots in POI_DATA.items():
        base_lat, base_lng = CITY_COORDS.get(city, (39.9, 116.4))
        
        for i, (name, category, tags) in enumerate(spots):
            # 生成略微不同的坐标
            lat = base_lat + (i % 5 - 2) * 0.02
            lng = base_lng + (i % 5 - 2) * 0.02
            
            # 生成评分和热度
            rating = round(4.3 + (hash(name) % 7) / 10, 1)  # 4.3-5.0
            heat = 7000 + (hash(name) % 3000)  # 7000-10000
            
            spot_data = {
                "name": name,
                "city": city,
                "category": category,
                "rating": rating,
                "heat_score": heat,
                "location_lat": lat,
                "location_lng": lng,
                "address": f"{city}市",
                "description": f"{name}是{city}的著名景点",
                "tags": tags,
                "open_time": "08:00-18:00",
                "ticket_price": "¥50"
            }
            
            spot = ScenicSpot(**spot_data)
            db.add(spot)
            spots_added += 1
            spot_id += 1
            
            # 每50条提交一次
            if spots_added % 50 == 0:
                db.commit()
                print(f"  Added {spots_added} spots...")
    
    db.commit()
    print(f"OK - Added {spots_added} spots total")
    
    db.close()
    print("\n=== Database Init Completed! ===")


if __name__ == "__main__":
    init_all_spots()
