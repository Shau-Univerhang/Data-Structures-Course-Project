def generate_food_data_code():
    """生成美食数据代码"""
    code = []
    code.append("// 生成美食数据")
    code.append("const generateFoodsData = () => {")
    code.append("  const foods = []")
    code.append("  let id = 1")
    code.append("")
    
    city_names = {
        "beijing": "北京",
        "shanghai": "上海",
    }
    
    for city, foods_list in FOOD_DATA.items():
        city_name = city_names.get(city, city)
        code.append(f"  // {city_name}美食 ({len(foods_list)}个)")
        code.append(f"  const {city}Foods = [")
        
        for food in foods_list:
            name, cuisine, rating, price, featured, tags, desc = food
            tags_str = ", ".join([f"'{t}'" for t in tags])
            code.append(f"    {{ name: '{name}', cuisine_type: '{cuisine}', rating: {rating}, price_range: '{price}', is_featured: {str(featured).lower()}, tags: [{tags_str}], description: '{desc}' }},")
        
        code.append("  ]")
        code.append(f"  {city}Foods.forEach(food => foods.push({{ id: id++, city: '{city_name}', ...food }}))")
        code.append("")
    
    code.append("  return foods")
    code.append("}")
    
    return "\n".join(code)

if __name__ == "__main__":
    print(generate_food_data_code())
